# 向函数传递参数
def printMyAddress(myName): # 将参数myName传入函数
    print(myName)  # 打印人名
    print("123 Main Stareet")
    print("Ottawa, Ontario, Canada")
    print("K2M 2E9")
    print()
printMyAddress("Carter Sande")  # 将Carter Sande 作为参数的值传入函数赋给其中的参数 myName

# Carter Sande
# 123 Main Stareet
# Ottawa, Ontario, Canada
# K2M 2E9